#include<iostream>
using namespace std;

int main(){

    /* ==	Equal to	x == y
    !=	Not equal	x != y
    >	Greater than	x > y
    <	Less than	x < y
    >=	Greater than or equal to	x >= y
    <=	Less than or equal to	x <= y */

    int x=5,y=10;

    cout << (x == y) << "\n" ;// false
    cout << (x!=y)<< "\n";// true
    cout << (x > y)<< "\n";// false
    cout << (x < y)<< "\n";// true
    cout << (x>=y)<< "\n"; // false
    cout << (x<=y)<< "\n";//true



    return 0;
}
